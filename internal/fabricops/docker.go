// DockerConfig holds parameters for generating a node's docker-compose.yaml.
type DockerConfig struct {
	NetworkName      string
	NetworkDomain    string
	OrgName          string
	MSPID            string
	Domain           string
	PeerPort         int
	ChaincodePort    int
	OpsPort          int
	CouchDBPort      int
	OrdererPort      int               // orderer listen port (default 7050)
	OrdererAdminPort int               // osnadmin port (default 7053)
	OrdererOpsPort   int               // orderer operations port (default 9443)
	GossipAddr       string            // host:port of a bootstrap peer for gossip
	IncludeOrderer   bool              // true for bootstrap node
	ExtraHosts       map[string]string // hostname → IP for cross‐host resolution
}

