func toLower(s string) string {
	b := []byte(s)
	for i, c := range b {
		if c >= 'A' && c <= 'Z' {
			b[i] = c + 32
		}
	}
	return string(b)
}

// GenerateBootstrapCompose creates docker-compose.yaml for orderer + peer + couchdb.
func GenerateBootstrapCompose(deployDir string, cfg DockerConfig) error {
	cfg.IncludeOrderer = true
	return renderCompose(deployDir, bootstrapComposeTmpl, cfg)
}

// GeneratePeerCompose creates docker-compose.yaml for a joining peer + couchdb only.
func GeneratePeerCompose(deployDir string, cfg DockerConfig) error {
	cfg.IncludeOrderer = false
	return renderCompose(deployDir, peerOnlyComposeTmpl, cfg)
}

// GenerateOrdererCompose creates docker-compose.yaml for a dedicated orderer only.
func GenerateOrdererCompose(deployDir string, cfg DockerConfig) error {
	if cfg.OrdererPort == 0 {
		cfg.OrdererPort = 7050
	}
	if cfg.OrdererAdminPort == 0 {
		cfg.OrdererAdminPort = 7053
	}
	if cfg.OrdererOpsPort == 0 {
		cfg.OrdererOpsPort = 9443
	}
	return renderCompose(deployDir, ordererOnlyComposeTmpl, cfg)
}

func renderCompose(deployDir, tmplText string, cfg DockerConfig) error {
	tmpl, err := template.New("compose").Funcs(funcMap).Parse(tmplText)
	if err != nil {
		return fmt.Errorf("parse compose template: %w", err)
	}

	path := filepath.Join(deployDir, "docker-compose.yaml")
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()
	return tmpl.Execute(f, cfg)
}

// EnsureDockerNetwork creates the shared kufichain_network if it doesn't exist.
// All compose files reference this as external, so it must exist before any
// docker compose up.
func EnsureDockerNetwork() error {
	// Check if network already exists
	out, _ := exec.Command("docker", "network", "ls", "--filter", "name=^kufichain_network$", "--format", "{{.Name}}").Output()
	if strings.TrimSpace(string(out)) == "kufichain_network" {
		return nil // already exists
	}
	cmd := exec.Command("docker", "network", "create", "kufichain_network")
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	return cmd.Run()
}

// composeProjectName derives a unique Docker Compose project name from the deploy
// directory path. This prevents cross-contamination when multiple nodes (orderer,
// peer1, peer2…) each have a "deploy" subdirectory on the same machine.
// Example: /path/.orderer/deploy → "kufi-orderer", /path/.peer1/deploy → "kufi-peer1"
func composeProjectName(deployDir string) string {
	parent := filepath.Dir(deployDir)
	base := filepath.Base(parent)
	base = strings.TrimPrefix(base, ".")
	if base == "" || base == "." {
		base = "default"
	}
	return "kufi-" + base
}

// StartContainers ensures the shared network exists, then runs docker compose up -d.
func StartContainers(deployDir string) error {
	if err := EnsureDockerNetwork(); err != nil {
		return fmt.Errorf("ensure docker network: %w", err)
	}
	p := composeProjectName(deployDir)
	return runCmd(deployDir, "docker", "compose", "-p", p, "up", "-d", "--remove-orphans")
}

// CleanupContainers tears down any existing containers and volumes.
func CleanupContainers(deployDir string) error {
	composePath := filepath.Join(deployDir, "docker-compose.yaml")
	if _, err := os.Stat(composePath); err != nil {
		return nil // no compose file, nothing to clean
	}
	p := composeProjectName(deployDir)
	return runCmd(deployDir, "docker", "compose", "-p", p, "down", "-v", "--remove-orphans")
}

// StopContainers runs docker compose down in the deploy directory.
func StopContainers(deployDir string) error {
	p := composeProjectName(deployDir)
	return runCmd(deployDir, "docker", "compose", "-p", p, "down", "--remove-orphans")
}

// CleanupLegacyNamedPeerContainers removes old fixed-name containers from
// previous kufichain versions to avoid conflicts with compose-managed names.
func CleanupLegacyNamedPeerContainers(domain, orgName string) {
	legacy := []string{
		fmt.Sprintf("peer0.%s", domain),
		fmt.Sprintf("couchdb.%s", toLower(orgName)),
	}
	for _, name := range legacy {
		_ = exec.Command("docker", "rm", "-f", name).Run()
	}
}

// ContainersRunning checks if the compose project already has running containers.
// Returns a list of running container names.
func ContainersRunning(deployDir string) []string {
	composePath := filepath.Join(deployDir, "docker-compose.yaml")
	if _, err := os.Stat(composePath); err != nil {
		return nil
	}
	p := composeProjectName(deployDir)
	out, err := runCmdOutput(deployDir, "docker", "compose", "-p", p, "ps", "--format", "{{.Name}}")
	if err != nil {
		return nil
	}
	var names []string
	for _, line := range strings.Split(strings.TrimSpace(string(out)), "\n") {
		line = strings.TrimSpace(line)
		if line != "" {
			names = append(names, line)
		}
	}
	return names
}

// ContainerStatus runs docker compose ps in the deploy directory.
func ContainerStatus(deployDir string) (string, error) {
	p := composeProjectName(deployDir)
	out, err := runCmdOutput(deployDir, "docker", "compose", "-p", p, "ps", "--format", "table")
	return string(out), err
}

// WaitForService polls docker compose until a service container is running.
func WaitForService(deployDir, service string, maxWait int) error {
	if maxWait <= 0 {
		maxWait = 1
	}
	p := composeProjectName(deployDir)
	var lastErr error
	for i := 0; i < maxWait; i++ {
		out, err := runCmdOutput(deployDir, "docker", "compose", "-p", p, "ps", "-q", service)
		if err == nil {
			containerID := strings.TrimSpace(string(out))
			if containerID != "" {
				stOut, inspectErr := runCmdOutput(".", "docker", "inspect", "-f", "{{.State.Status}}", containerID)
				if inspectErr == nil {
					status := strings.TrimSpace(string(stOut))
					if status == "running" {
						return nil
					}
					if status == "exited" || status == "dead" {
						return fmt.Errorf("service %s container %s", service, status)
					}
				} else {
					lastErr = inspectErr
				}
			}
		} else {
			lastErr = err
		}
		time.Sleep(1 * time.Second)
	}
	if lastErr == nil {
		lastErr = fmt.Errorf("service not started")
	}
	return fmt.Errorf("service %s not running after %ds: %w", service, maxWait, lastErr)
}

// WaitForContainer polls docker until a container reports healthy/running.
func WaitForContainer(name string, maxWait int) error {
	for i := 0; i < maxWait; i++ {
		out, _ := runCmdOutput(".", "docker", "inspect", "-f", "{{.State.Status}}", name)
		status := strings.TrimSpace(string(out))
		if status == "running" {
			return nil
		}
		if status == "exited" || status == "dead" {
			return fmt.Errorf("container %s %s", name, status)
		}
		time.Sleep(1 * time.Second)
	}
	return fmt.Errorf("container %s not running after %ds", name, maxWait)
}
